-- ============================================================
-- 6. VERIFICATION STATUS TRIGGER
-- ============================================================
CREATE OR REPLACE TRIGGER trg_enforce_verified_status
BEFORE INSERT OR UPDATE ON service_lines
FOR EACH ROW
DECLARE
    v_has_photo  NUMBER := 0;
    v_has_frb    NUMBER := 0;
    v_mat_ok     NUMBER := 0;
    v_composite  VARCHAR2(50);
BEGIN
    v_composite := CASE
        WHEN :NEW.system_owned = 1 AND :NEW.service_line_material = 'lead' THEN 'System_Lead'
        WHEN :NEW.customer_owned = 1 AND :NEW.customer_side_material = 'lead' THEN 'Customer_Lead'
        WHEN :NEW.system_owned = 1 AND :NEW.service_line_material = 'galvanized_requiring_replacement' THEN 'Galvanized_Requiring_Replacement'
        WHEN :NEW.customer_owned = 1 AND :NEW.customer_side_material = 'galvanized_requiring_replacement' THEN 'Galvanized_Requiring_Replacement'
        ELSE 'Non-Lead_Or_Unknown'
    END;

    IF :NEW.id IS NOT NULL THEN
        SELECT COUNT(*) INTO v_has_photo FROM evidence
        WHERE service_line_id = :NEW.id AND evidence_type = 'photo'
        AND gps_accuracy_meters <= 2 AND ROWNUM = 1;

        IF v_composite IN ('System_Lead','Customer_Lead','Galvanized_Requiring_Replacement') THEN
            SELECT COUNT(*) INTO v_has_frb FROM pfas_sampling
            WHERE service_line_id = :NEW.id AND sample_type = 'FRB'
            AND received_by_lab IS NOT NULL AND ROWNUM = 1;
        ELSE
            v_has_frb := 1;
        END IF;
    ELSE
        v_has_frb := 1;
    END IF;

    IF :NEW.system_owned = 0 OR NVL(:NEW.service_line_material,'unknown') != 'unknown' THEN
        v_mat_ok := 1;
    END IF;

    IF v_has_photo = 1 AND v_has_frb = 1 AND v_mat_ok = 1 THEN
        :NEW.verified_status := 1;
        :NEW.verified_at := SYSTIMESTAMP;
    ELSE
        :NEW.verified_status := 0;
        :NEW.verified_at := NULL;
        :NEW.verified_by := NULL;
    END IF;

    :NEW.last_updated := SYSTIMESTAMP;
END;
/

-- ============================================================
-- 7. PFAS SEQUENCE ORDER TRIGGER
-- ============================================================
CREATE OR REPLACE TRIGGER trg_check_pfas_sequence_order
BEFORE INSERT OR UPDATE ON pfas_sampling
FOR EACH ROW
DECLARE
    v_has_regular NUMBER := 0;
BEGIN
    IF :NEW.batch_id IS NOT NULL AND :NEW.sample_type IN ('FRB','EB','TB') THEN
        SELECT COUNT(*) INTO v_has_regular FROM pfas_sampling
        WHERE batch_id = :NEW.batch_id AND sample_type = 'regular'
        AND sequence_number < :NEW.sequence_number AND ROWNUM = 1;

        IF v_has_regular = 1 THEN
            RAISE_APPLICATION_ERROR(-20001,
                'FRB, EB, or TB must precede regular samples in sequence order per ODEQ 2026');
        END IF;
    END IF;
END;
/

